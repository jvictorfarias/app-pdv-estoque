    private void jbAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbAlterarActionPerformed
        // TODO add your handling code here:
        controlaCampos(true);
        globalAlter = "alter";
        int linha = this.jtProduto.getSelectedRow();
        try {
            int codigoProduto = (int) this.jtProduto.getValueAt(linha, 0);
            // Vai até o banco
            modelProduto = controllerProduto.readProdutoController(codigoProduto);
            this.jtfCodigo.setText(String.valueOf(modelProduto.getIdProduto()));
            this.jtfNome.setText(modelProduto.getNomeProduto());
            this.jtfEstoque.setText(String.valueOf(modelProduto.getEstoqueProduto()));
            this.jtfValor.setText(String.valueOf(modelProduto.getValorProduto()));
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Codigo inválido ou registro não selecionado", "ERRO", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jbAlterarActionPerformed

