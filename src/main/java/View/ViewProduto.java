    private void jbBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbBuscarActionPerformed
        // TODO add your handling code here:
        DefaultTableModel tabelaProdutos = (DefaultTableModel) this.jtProduto.getModel();
        final TableRowSorter<TableModel> sorter = new TableRowSorter<>(tabelaProdutos);
        this.jtProduto.setRowSorter(sorter);
        String busca = jtfPesquisa.getText().toUpperCase();
        sorter.setRowFilter(RowFilter.regexFilter(busca, 1));
    }//GEN-LAST:event_jbBuscarActionPerformed

    private void jbExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbExcluirActionPerformed
        // TODO add your handling code here:
        int linha = jtProduto.getSelectedRow();
        int codigoProduto = (int) jtProduto.getValueAt(linha, 0);
        if (controllerProduto.deleteProdutoController(codigoProduto)) {
            JOptionPane.showMessageDialog(this, "Produto excluído.", "Aviso", JOptionPane.WARNING_MESSAGE);
            carregaProdutos();
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao excluir o produto", "ERRO", JOptionPane.ERROR_MESSAGE);
        }

    }//GEN-LAST:event_jbExcluirActionPerformed

